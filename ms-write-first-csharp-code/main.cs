// WriteLine adds a newline at the end of this print phrase
Console.WriteLine("Hello World!");
// Write keeps all the print statements on the same line
Console.Write("Congratulations!");
Console.Write(" ");
Console.Write("You just wrote your first line of C# code.");

// Compilers convert human code to an executable format
// Member access operator = . between the class and method
// Method invocation operator = () after a method
// End-of-statement operator = ;
// Console is a class, Write and WriteLine are methods
